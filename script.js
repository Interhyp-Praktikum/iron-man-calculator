const calculateStat = ({
  selectedHelmet,
  selectedBody,
  selectedLegs,
  selectedArms,
  helmetWeight = 0.5,
  bodyWeight = 2,
  key
}) => {
  return (
    (selectedHelmet[key] * helmetWeight +
      selectedBody[key] * bodyWeight +
      selectedLegs[key] +
      selectedArms[key]) /
    4
  );
};

